#include "stdafx.h"
#include "Entity.h"
//-----------------------------------------------------------------------------
Entity::Entity(const std::string& fileName)
{
	m_sprite = new Sprite(fileName);
}
//-----------------------------------------------------------------------------
Entity::~Entity()
{
	delete m_sprite;
}
//-----------------------------------------------------------------------------
void Entity::Draw()
{
	m_sprite->Draw(m_position, SizeTile);
}
